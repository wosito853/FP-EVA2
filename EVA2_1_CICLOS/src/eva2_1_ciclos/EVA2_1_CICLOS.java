 // ciclo for - cuando sabe,os cuantas veces se repetirá una instrucciión
        // funciona como un contador (inicio, fin(condición) y salto de conteo)
        // vamos a repetir 10 veces contando del 1 a 10
        //nosotros estavblecemos el úto de partida y punto de termino
        for(int i = 1; i <= 10; i++ ){
            System.out.print(i + "-");
        }
        System.out.println("");
        for(int i = 10; i >= 1; i-- ){
            System.out.print(i + "-");
        }
    
 
        
        
        
        
        
